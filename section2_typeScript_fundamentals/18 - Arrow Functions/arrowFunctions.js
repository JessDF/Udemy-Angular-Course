//can declare a function like this in JavaScript:
var jsFunc = function (message) {
    console.log(message);
};
//In typescript we can do:
var tsFunc = function (message) {
    console.log(message);
};
//If function is only one line we can:
var tsFunc2 = function (message) { return console.log(message); };
//These are called Arrow Functions
//If have no parameters:
var tsFunc3 = function () { return console.log("No Parameters"); };
